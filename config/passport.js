//jshint esversion:8
const LocalStrategy = require('passport-local').Strategy; // calling the passport local node module package
const bcrypt = require('bcryptjs'); // this is a node module package for the encryption 

// Load User model
const User = require('../models/User');

// this module will authenticate a user wheather that user has valid email or registered password or not if not then he can  not have the access of account.
module.exports = function(passport) {
  passport.use(
    new LocalStrategy({ usernameField: 'email' }, (email, password, done) => {
      // Match user
      User.findOne({
        email: email
      }).then(user => {
        if (!user) {
          return done(null, false, { message: 'That email is not registered' });
        }

        // Match password
        bcrypt.compare(password, user.password, (err, isMatch) => {
          if (err) throw err;
          if (isMatch) {
            return done(null, user);
          } else {
            return done(null, false, { message: 'Password incorrect' });
          }
        });
      });
    })
  );

  passport.serializeUser(function(user, done) {
    done(null, user.id);
  });

  passport.deserializeUser(function(id, done) {
    User.findById(id, function(err, user) {
      done(err, user);
    });
  });
};
